"""
THEORY_PART_CCCI_QUANTUM_GROUPS_HOPF.py
Pillar 201 -- Quantum Groups & Hopf Algebras from W(3,3)

Quantum groups, introduced by Drinfeld and Jimbo (1985), are deformations
of universal enveloping algebras U(g) parametrized by q. They carry Hopf
algebra structure (coproduct, counit, antipode) and produce solutions to
the Yang-Baxter equation via R-matrices. Their representation theory leads
to braided tensor categories, knot invariants (Jones polynomial, HOMFLY-PT),
and topological quantum field theories (Witten-Reshetikhin-Turaev).

Khovanov homology (2000) categorifies the Jones polynomial, lifting it to
a bigraded homology theory. KLR algebras (Khovanov-Lauda/Rouquier 2008)
categorify quantum groups themselves.

W(3,3) connection: The symplectic group Sp(6,F2) of order 1451520 is the
automorphism group of W(3,3). The quantum group U_q(sp(6)) at roots of
unity over F2 connects the 40 points of W(3,3) to crystal bases and
R-matrices from the incidence geometry.

References:
  Drinfeld (1985), Jimbo (1985), Jones (1984), Kassel (1995),
  Khovanov (2000), Khovanov-Lauda (2008), Rouquier (2008),
  Reshetikhin-Turaev (1991), Kashaev (1997)
"""

