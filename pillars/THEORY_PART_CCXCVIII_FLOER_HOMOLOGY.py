"""
THEORY_PART_CCXCVIII_FLOER_HOMOLOGY.py
Pillar 198 -- Floer Homology from W(3,3)

Floer homology, introduced by Andreas Floer (1988), is an infinite-
dimensional analogue of Morse theory that has revolutionised symplectic
topology, low-dimensional topology, and mathematical physics.  Starting
from the action functional on the loop space of a symplectic manifold,
Floer defined a chain complex whose differential counts pseudo-holomorphic
curves (Gromov 1985) connecting critical points -- the 1-periodic orbits of
a Hamiltonian system.  The resulting Hamiltonian Floer homology proved the
Arnold conjecture in important cases, establishing that the number of
fixed points of a Hamiltonian diffeomorphism is bounded below by the sum
of the Betti numbers.

Lagrangian Floer homology, systematised by Fukaya-Oh-Ohta-Ono (FOOO),
promotes Lagrangian submanifolds to objects of the Fukaya category, an
A-infinity category whose morphism spaces are Floer cochain complexes.
Bounding cochains and obstruction theory handle disc bubbling.  Homological
mirror symmetry (Kontsevich 1994) identifies the Fukaya category of the
symplectic side with the derived category of coherent sheaves on the mirror.

Heegaard Floer homology (Ozsvath-Szabo 2004) provides powerful invariants
of 3-manifolds and knots.  Knot Floer homology detects the Seifert genus
(Ozsvath-Szabo 2004) and fibredness, while concordance invariants tau and
epsilon obstruct smooth concordance.  Bordered Floer homology
(Lipshitz-Ozsvath-Thurston) extends these invariants to 3-manifolds with
boundary via type-D and type-A structures.

Gauge-theoretic Floer theories include instanton Floer homology (Floer
1988), monopole Floer homology (Kronheimer-Mrowka), and Seiberg-Witten
Floer homology.  The Atiyah-Floer conjecture relates instanton Floer
homology to Lagrangian Floer homology in the moduli space of flat
connections.  Manolescu's pin(2)-equivariant Seiberg-Witten Floer homology
(2016) disproved the triangulation conjecture in dimensions >= 5.

The W(3,3) symplectic polar space -- 40 isotropic points in PG(5,F_2),
automorphism group Sp(6,F_2) of order 1451520 -- provides a finite
combinatorial model for Floer-theoretic structures.  The 40 isotropic
points serve as generators of a finite Floer complex, and the Lagrangian
subspaces of the underlying GF(2)^6 symplectic space model Lagrangian
submanifolds.  The Fukaya category of W(3,3) Lagrangians captures the
A-infinity structure in finite characteristic.  The Arnold conjecture
applied to W(3,3) asserts at least 40 fixed points, matching the
40 isotropic points.

References:
  Floer (1988), Gromov (1985), Arnold (1965), Fukaya-Oh-Ohta-Ono (2009),
  Kontsevich (1994), Ozsvath-Szabo (2004), Lipshitz-Ozsvath-Thurston (2018),
  Kronheimer-Mrowka (2007), Manolescu (2016), Oh (2005), Schwarz (2000),
  Hofer-Zehnder (1994), Cohen-Jones-Segal (1995), Seidel (2008)
"""

