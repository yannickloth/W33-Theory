# DIMENSION FORMULA
# ══════════════════════════════════════════════════════════════

def dimension_formula():
    """
    Dimension of the space of modular forms of weight k for SL(2,Z):

    For even k >= 0:
      dim M_k = floor(k/12)     if k ≡ 2 (mod 12)
      dim M_k = floor(k/12) + 1 otherwise

    For k < 0 or k odd:  dim = 0
    For k = 0: dim = 1 (constants)
    For k = 2: dim = 0 (no nontrivial weight-2 forms for SL(2,Z))

    dim S_k (cusp forms) = dim M_k - 1 for k >= 2 (subtracting Eisenstein).
    """
    dims = {}
    for k in range(0, 50, 2):
        if k == 0:
            dims[k] = 1
        elif k == 2:
            dims[k] = 0
        elif k % 12 == 2:
            dims[k] = k // 12
        else:
            dims[k] = k // 12 + 1

    cusp_dims = {k: max(v - 1, 0) for k, v in dims.items() if k >= 2}

    return {
        'dims': dims,
        'cusp_dims': cusp_dims,
        'first_cusp_form_weight': 12,   # Δ is first cusp form
        'dim_12': dims[12],             # = 2 (E_4^3 and Δ)
        'cusp_dim_12': cusp_dims[12],   # = 1   (Δ alone)
        'dim_24': dims[24],             # = 3
        'dim_36': dims[36],             # = 4
        'formula': 'floor(k/12)+1 or floor(k/12) if k≡2(mod12)',
    }


