def build_voltage_functor_report() -> dict:
    out: dict = {"status": "ok"}

    # Load K generators on 54 pockets
    gen_data = json.loads(
        (WELD_BUNDLE / "K_generators_action_on_pockets_54.json").read_text()
    )
    gens_K = {name: tuple(gen_data[name]) for name in ["g2", "g3", "g5", "g8", "g9"]}
    n_pockets = 54
    idn_K = tuple(range(n_pockets))

    # Load tomotope generators (192 flags)
    with zipfile.ZipFile(TOMOTOPE_ZIP) as zf:
        rgens = json.loads(zf.read("tomotope_r_generators_192.json"))
    tr0 = tuple(rgens["r0"])
    tr1 = tuple(rgens["r1"])
    tr2 = tuple(rgens["r2"])
    tr3 = tuple(rgens["r3"])
    n_flags = 192

    # Compute t = r1*r2, t^4
    t = compose(tr1, tr2)
    t2 = compose(t, t)
    t4 = compose(t2, t2)
    t8 = compose(t4, t4)

    # ==================================================================
    # T1: K-Schreier voltage stabilizer word of order 3
    # ==================================================================
    # Cycle: 0 -> 3 -> 13 -> 32 -> 5 -> 0
    # Generators: g5, g5, g3, g5, g2 (from voltage functor bundle)
    with zipfile.ZipFile(FUNCTOR_BUNDLE) as zf:
        stab_data = json.loads(
            zf.read(FUNCTOR_BASE + "/K_stabilizer_C3_generator_from_cycle.json")
        )

    # Verify the 5-cycle and voltage
    cycle_nodes = stab_data["cycle_nodes"]
    edges = stab_data["edges"]
    assert len(cycle_nodes) == 6 and cycle_nodes[0] == cycle_nodes[-1] == 0

    # Verify each edge step
    for e in edges:
        u, v, gen = e["u"], e["v"], e["gen"]
        assert gens_K[gen][u] == v, f"Edge {u}->{v} via {gen} does not match"

    # Verify total voltage = 1 mod 3
    total_voltage = sum(e["exp"] for e in edges) % 3
    assert total_voltage == 1, f"Total voltage = {total_voltage}, expected 1"
    assert stab_data["cycle_voltage_sum_mod3"] == 1

    # Build the stabilizer word by composing generators (left-to-right application):
    # w(x) = g2(g5(g3(g5(g5(x))))) so compose outermost last
    word = stab_data["stabilizer_word"]  # ["g5", "g5", "g3", "g5", "g2"]
    w = idn_K
    for gen_name in word:
        # compose(g, w)(x) = g(w(x)) — prepend each next generator
        w = compose(gens_K[gen_name], w)

    # Verify w fixes pocket 0
    assert w[0] == 0, f"Stabilizer word does not fix pocket 0: w[0]={w[0]}"

    # Verify order of w on 54 pockets = 3
    ord_w = perm_order(w)
    assert ord_w == 3, f"Stabilizer word order = {ord_w}, expected 3"

    # Verify the permutation matches the bundle
    expected_perm = tuple(stab_data["perm_on_pocket_ids"])
    assert w == expected_perm, "Stabilizer word permutation does not match bundle"

    out["T1_cycle_nodes"] = cycle_nodes
    out["T1_word"] = word
    out["T1_total_voltage"] = total_voltage
    out["T1_word_order"] = ord_w
    out["T1_fixes_pocket0"] = True
    out["T1_verified"] = True
    print(
        f"T1: stabilizer word {word} has order {ord_w}, total voltage=1, "
        f"fixes pocket 0  OK"
    )

    # ==================================================================
    # T2: Voltage functor Phi: Z3 -> Aut(192), exp |-> (t^4)^exp
    # ==================================================================
    # Phi(0) = identity, Phi(1) = t^4, Phi(2) = (t^4)^2 = t^8
    idn_flags = tuple(range(n_flags))

    # Phi is a group homomorphism: Phi(a+b mod 3) = Phi(a) o Phi(b)
    phi = [idn_flags, t4, t8]

    # Verify ord(t^4) = 3 so Phi is well-defined
    assert perm_order(t4) == 3
    # Verify t^8 = (t^4)^2
    assert t8 == compose(t4, t4)
    # Verify (t^4)^3 = identity
    assert compose(t4, compose(t4, t4)) == idn_flags

    # Homomorphism: Phi(a+b) = Phi(a) o Phi(b) for all a,b in Z3
    for a in range(3):
        for b in range(3):
            assert phi[(a + b) % 3] == compose(phi[a], phi[b]), (
                f"Phi not a homomorphism at a={a}, b={b}"
            )

    # Injectivity: Phi(1) != Phi(0), Phi(2) != Phi(0), Phi(1) != Phi(2)
    assert phi[1] != phi[0], "Phi(1) = identity => not injective"
    assert phi[2] != phi[0], "Phi(2) = identity => not injective"
    assert phi[1] != phi[2], "Phi(1) = Phi(2) => not injective"

    out["T2_functor_well_defined"] = True
    out["T2_functor_homomorphism"] = True
    out["T2_functor_injective"] = True
    out["T2_t4_order"] = 3
    print("T2: voltage functor Phi: Z3 -> Aut(192) is a well-defined injective homomorphism  OK")

    # ==================================================================
    # T3: Universal 2+1+1 block split for all 48 blocks
    # ==================================================================
    # 48 blocks = orbits of <r0, r3> on 192 flags
    block_orb = get_orbits([tr0, tr3], n_flags)
    n_blocks = max(block_orb) + 1
    assert n_blocks == 48

    blocks: list[list[int]] = [[] for _ in range(n_blocks)]
    for f in range(n_flags):
        blocks[block_orb[f]].append(f)

    # For each block, count how many flags map to the SAME block under t^4
    self_counts: list[int] = []
    for bid, block in enumerate(blocks):
        self_count = sum(1 for f in block if block_orb[t4[f]] == bid)
        self_counts.append(self_count)

    # Verify all blocks have self-count = 2
    assert all(c == 2 for c in self_counts), (
        f"Self-count distribution: {Counter(self_counts)}"
    )

    # Verify the other 2 flags go to 2 distinct blocks (both different from B)
    for bid, block in enumerate(blocks):
        away_blocks = {block_orb[t4[f]] for f in block if block_orb[t4[f]] != bid}
        assert len(away_blocks) == 2, (
            f"Block {bid}: away flags go to {len(away_blocks)} blocks, expected 2"
        )

    # Cross-check against bundle CSV
    with zipfile.ZipFile(FUNCTOR_BUNDLE) as zf:
        split_txt = zf.read(
            FUNCTOR_BASE + "/tomotope_t4_block_split_details.csv"
        ).decode()
    split_rows = list(csv.DictReader(split_txt.splitlines()))
    assert len(split_rows) == 48

    # Each row: "self:2 other1:1 other2:1" pattern
    for row in split_rows:
        pattern = row["pattern"]
        parts = pattern.split()
        counts = [int(p.split(":")[1]) for p in parts]
        self_count = next(
            int(p.split(":")[1])
            for p in parts
            if int(p.split(":")[0]) == int(row["block"])
        )
        assert self_count == 2, f"Block {row['block']}: self-count={self_count}"
        assert sorted(counts) == [1, 1, 2], f"Block {row['block']}: pattern {counts}"

    out["T3_n_blocks"] = 48
    out["T3_universal_2plus1plus1"] = True
    out["T3_self_count"] = 2
    out["T3_away_count"] = 2
    print("T3: universal 2+1+1 block split verified for all 48 blocks  OK")

    # ==================================================================
    # T4: 16 tomotope faces partition into 4 t^4-invariant groups of 4
    # ==================================================================
    # Faces (rank-2 cells) = orbits of <r0, r1, r3> on 192 flags
    face_orb = get_orbits([tr0, tr1, tr3], n_flags)
    n_faces = max(face_orb) + 1
    assert n_faces == 16, f"n_faces={n_faces}, expected 16"

    # Build face flag lists
    faces: list[list[int]] = [[] for _ in range(n_faces)]
    for f in range(n_flags):
        faces[face_orb[f]].append(f)
    assert all(len(fc) == 12 for fc in faces), "Faces not all size 12"

    # t^4 does NOT map faces to faces (flags scatter across ≤4 faces per block).
    # Instead, build a mixing graph: faces i and j are adjacent if some flag of
    # face i maps into face j under t^4.
    face_adj: dict[int, set[int]] = {i: set() for i in range(n_faces)}
    for i in range(n_faces):
        for f in faces[i]:
            j = face_orb[t4[f]]
            if i != j:
                face_adj[i].add(j)
                face_adj[j].add(i)

    # Find connected components of the mixing graph
    face_vis = [-1] * n_faces
    fc_id = 0
    for s in range(n_faces):
        if face_vis[s] == -1:
            q = [s]; face_vis[s] = fc_id
            while q:
                v = q.pop()
                for w in face_adj[v]:
                    if face_vis[w] == -1:
                        face_vis[w] = fc_id; q.append(w)
            fc_id += 1

    n_face_comp = fc_id
    face_comp_sizes = Counter(face_vis)
    comp_sizes_list = sorted(face_comp_sizes.values())

    assert n_face_comp == 4, f"Face triality components: {n_face_comp}, expected 4"
    assert comp_sizes_list == [4, 4, 4, 4], (
        f"Face component sizes: {comp_sizes_list}"
    )

    # Verify each component's flag set (48 flags) is t^4-invariant
    for c in range(n_face_comp):
        comp_flags = set()
        for i in range(n_faces):
            if face_vis[i] == c:
                comp_flags.update(faces[i])
        assert all(t4[f] in comp_flags for f in comp_flags), (
            f"Component {c} flag set not t^4-invariant"
        )

    # Cross-check against bundle data
    with zipfile.ZipFile(FUNCTOR_BUNDLE) as zf:
        face_part = json.loads(
            zf.read(
                FUNCTOR_BASE + "/tomotope_faces_partition_by_triality_components.json"
            )
        )
    bundle_partition = face_part["face_partition_by_component"]
    assert len(bundle_partition) == 4
    for comp_faces in bundle_partition.values():
        assert len(comp_faces) == 4

    out["T4_n_faces"] = n_faces
    out["T4_face_triality_components"] = n_face_comp
    out["T4_face_component_sizes"] = [4, 4, 4, 4]
    out["T4_flag_sets_t4_invariant"] = True
    print(f"T4: 16 faces form {n_face_comp} t^4-invariant groups of 4 (48 flags each)  OK")

    # ==================================================================
    # T5: 48 blocks partition into 4 triality components of 12 each
    # ==================================================================
    # Same graph-mixing approach: blocks i,j adjacent if a flag of block i
    # maps into block j under t^4.
    block_adj: dict[int, set[int]] = {b: set() for b in range(n_blocks)}
    for b in range(n_blocks):
        for f in blocks[b]:
            j = block_orb[t4[f]]
            if b != j:
                block_adj[b].add(j)
                block_adj[j].add(b)

    block_vis = [-1] * n_blocks
    bc_id = 0
    for s in range(n_blocks):
        if block_vis[s] == -1:
            q = [s]; block_vis[s] = bc_id
            while q:
                v = q.pop()
                for w in block_adj[v]:
                    if block_vis[w] == -1:
                        block_vis[w] = bc_id; q.append(w)
            bc_id += 1

    n_block_comp = bc_id
    block_comp_sizes = Counter(block_vis)

    assert n_block_comp == 4, f"Block triality components: {n_block_comp}, expected 4"
    assert all(v == 12 for v in block_comp_sizes.values()), (
        f"Block component sizes: {dict(block_comp_sizes)}"
    )

    # Cross-check against bundle triangle graph CSV
    with zipfile.ZipFile(FUNCTOR_BUNDLE) as zf:
        tri_txt = zf.read(
            FUNCTOR_BASE + "/tomotope_t4_triangle_graph_components.csv"
        ).decode()
    tri_rows = list(csv.DictReader(tri_txt.splitlines()))
    assert len(tri_rows) == 48
    bundle_block_comps = Counter(r["component"] for r in tri_rows)
    assert len(bundle_block_comps) == 4
    assert all(v == 12 for v in bundle_block_comps.values())

    out["T5_n_blocks"] = 48
    out["T5_block_triality_components"] = n_block_comp
    out["T5_block_component_sizes"] = [12, 12, 12, 12]
    out["T5_block_component_uniform"] = True
    print(f"T5: 48 blocks form {n_block_comp} t^4-connected components of 12 each  OK")

    # ==================================================================
    # T6: Functor coherence — Z3 voltage, H-triality, tomotope all agree
    # ==================================================================
    # Load voltage functor dictionary from bundle
    with zipfile.ZipFile(FUNCTOR_BUNDLE) as zf:
        vfd = json.loads(
            zf.read(FUNCTOR_BASE + "/voltage_functor_dictionary.json")
        )

    # Verify voltage -> H-index -> order mapping
    kv = vfd["K_voltage_to_axis_H_triality_elements"]
    assert int(kv["0"]["H_index"]) == 7   and int(kv["0"]["order"]) == 1
    assert int(kv["1"]["H_index"]) == 71  and int(kv["1"]["order"]) == 3
    assert int(kv["2"]["H_index"]) == 46  and int(kv["2"]["order"]) == 3

    # Verify tomotope side: (t^4)^exp orders
    phi_orders = [perm_order(phi[exp]) for exp in range(3)]
    assert phi_orders == [1, 3, 3], f"Phi orders: {phi_orders}"

    # Coherence check: both sides have the same order profile (1, 3, 3) for exp (0, 1, 2)
    h_orders = [int(kv[str(exp)]["order"]) for exp in range(3)]
    assert h_orders == phi_orders, (
        f"H-orders {h_orders} != tomotope phi-orders {phi_orders}"
    )

    # Verify t^4 != identity, (t^4)^2 != identity, (t^4)^3 == identity
    assert phi[1] != phi[0]  # t^4 != id
    assert phi[2] != phi[0]  # (t^4)^2 != id
    assert phi[1] != phi[2]  # t^4 != (t^4)^2
    assert compose(phi[1], phi[2]) == phi[0]  # t^4 * (t^4)^2 = id

    # All three representations (K-voltage, H-index, tomotope) are Z3
    # with the same order profile — they are isomorphic images of the same Z3
    out["T6_voltage_H_orders"] = h_orders
    out["T6_voltage_tomotope_orders"] = phi_orders
    out["T6_orders_match"] = True
    out["T6_functor_coherent"] = True
    out["T6_H_exp1_index"] = 71
    out["T6_H_exp2_index"] = 46
    out["T6_phi1_order"] = phi_orders[1]
    out["T6_phi2_order"] = phi_orders[2]
    print(
        f"T6: Z3 orders match: voltage={h_orders}, tomotope={phi_orders}; "
        f"functor coherent  OK"
    )

    # Summary
    out["summary"] = {
        "voltage_functor_Phi": "Z3 -> Aut(192 flags), exp |-> (t^4)^exp",
        "generator": "5-cycle [g5,g5,g3,g5,g2], voltage=1, word order=3",
        "block_split": "universal 2+1+1 on all 48 blocks",
        "face_components": 4,
        "block_components": 4,
        "coherence": "K-voltage, H-triality, tomotope t^4 all represent same Z3",
    }
    return out


def main() -> None:
    report = build_voltage_functor_report()
    out_path = ROOT / "data" / "w33_voltage_functor.json"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(report, indent=2), encoding="utf-8")
    print(f"Wrote {out_path}")
    print("All theorems verified OK")


if __name__ == "__main__":
    main()
