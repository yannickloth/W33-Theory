"""Pillar 73 (Part CLXXXI): The Voltage Functor K -> Tomotope

Six theorems closing the loop between the K-pocket Schreier voltage (Z3
labels on 270 directed edges) and the tomotope's internal triality element
t^4 = (r1*r2)^4:

  T1  The K-Schreier voltage is generated by an explicit order-3 stabilizer
      word w = [g5, g5, g3, g5, g2], witnessed by the 5-cycle
      0 -> 3 -> 13 -> 32 -> 5 -> 0 with total voltage 1 (mod 3).

  T2  The voltage functor Phi: Z3 -> Aut(192 flags),  exp |-> (t^4)^exp,
      is a well-defined injective group homomorphism (ker Phi = {0}).

  T3  Universal 2+1+1 block split: for EVERY one of the 48 tomotope blocks
      B, exactly 2 of B's 4 flags are fixed within B under t^4, while the
      remaining 2 flags map to two distinct other blocks (one each).

  T4  The 16 tomotope faces partition into 4 triality components of 4 faces
      each, invariant under the induced t^4 action on faces.

  T5  The 48 tomotope blocks partition into 4 triality components of 12
      blocks each, matching the triangle-graph connectivity from T4 in
      Pillar 72.

  T6  Functor coherence: the Z3 voltage exponent (exp = 0, 1, 2) on K-
      Schreier edges, the H-triality element (H-indices 7, 71, 46), and the
      tomotope power (t^4)^exp all represent the SAME abstract Z3 element.
      Verified: both H-index 71 and (t^4)^1 have order 3; H-index 46 and
      (t^4)^2 both have order 3; and H-index 7 and (t^4)^0 are both the
      respective identities.
"""
