# ---------------------------------------------------------------------------
# Generic permutation helpers
# ---------------------------------------------------------------------------

def compose(p: tuple, q: tuple) -> tuple:
    return tuple(p[q[i]] for i in range(len(p)))


def perm_order(p: tuple) -> int:
    n = len(p)
    idn = tuple(range(n))
    cur = p
    k = 1
    while cur != idn:
        cur = compose(p, cur)
        k += 1
        if k > 300_000:
            raise RuntimeError("order overflow")
    return k


def get_orbits(gens: list[tuple], n: int) -> list[int]:
    visited = [-1] * n
    oid = 0
    for s in range(n):
        if visited[s] == -1:
            q = [s]
            visited[s] = oid
            while q:
                v = q.pop()
                for a in gens:
                    w = a[v]
                    if visited[w] == -1:
                        visited[w] = oid
                        q.append(w)
            oid += 1
    return visited


def subgroup_closure(gens: list[tuple]) -> set[tuple]:
    if not gens:
        return set()
    n = len(gens[0])
    idn = tuple(range(n))
    S: set = {idn}
    queue = list(gens)
    while queue:
        g = queue.pop()
        if g in S:
            continue
        S.add(g)
        for h in list(S):
            for prod in (compose(g, h), compose(h, g)):
                if prod not in S:
                    queue.append(prod)
    return S


# ---------------------------------------------------------------------------
# Main
