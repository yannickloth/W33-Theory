def omega_symplectic(v, w):
    """
    Standard symplectic form on Fâ‚ƒâپ´:
    د‰((a,b,c,d), (a',b',c',d')) = ab' - a'b + cd' - c'd (mod 3)
    """
    return (v[0]*w[1] - v[1]*w[0] + v[2]*w[3] - v[3]*w[2]) % 3

def normalize_f3(v):
    """Normalize vector: make first nonzero coordinate = 1"""
    for i, x in enumerate(v):
        if x != 0:
            inv = pow(int(x), -1, 3)  # Inverse in Fâ‚ƒ: 1âپ»آ¹=1, 2âپ»آ¹=2
            return tuple((inv * c) % 3 for c in v)
    return v

