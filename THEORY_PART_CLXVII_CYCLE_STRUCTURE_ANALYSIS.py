def compute_vertex_permutation(M, vertices, vertex_to_idx):
    """
    Compute permutation of vertices induced by matrix M

    Returns: list perm where perm[i] = j means M sends vertex i to vertex j
    """
    perm = []
    for v in vertices:
        Mv = apply_matrix_f3(M, v)
        j = vertex_to_idx[Mv]
        perm.append(j)
    return perm

def vertex_perm_to_edge_perm(vertex_perm, edges):
    """
    Given permutation of vertices, compute induced permutation of edges

    Edge (i,j) maps to edge (perm[i], perm[j])
    """
    edge_perm = []
    for i, j in edges:
        # Apply vertex permutation
        new_i, new_j = vertex_perm[i], vertex_perm[j]
        # Find this edge in the edge list
        new_edge = tuple(sorted([new_i, new_j]))
        # Find index of new edge
        try:
            new_idx = edge_to_idx[new_edge]
        except KeyError:
            print(f"ERROR: Edge {new_edge} not found!")
            new_idx = -1
        edge_perm.append(new_idx)

    return edge_perm

def cycle_decomposition(perm):
    """
    Decompose permutation into cycles

    Returns: list of cycles (as lists of indices)
    """
    n = len(perm)
    visited = [False] * n
    cycles = []

    for start in range(n):
        if visited[start]:
            continue

        cycle = []
        i = start
        while not visited[i]:
            visited[i] = True
            cycle.append(i)
            i = perm[i]

        if len(cycle) > 1:  # Skip fixed points initially
            cycles.append(cycle)

    return cycles

def cycle_type(cycles):
    """
    Return cycle type as dictionary: {length: count}
    """
    lengths = [len(c) for c in cycles]
    return dict(Counter(lengths))

