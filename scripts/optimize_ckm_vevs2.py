#!/usr/bin/env python3
"""Extended CKM optimizer using random convex combinations.

This script reads the best up/down vertex indices from
`data/w33_yukawa_blocks.json` (generated by `w33_yukawa_blocks.py`) and then
performs a stochastic search over linear combinations of the original best
vertices and their H27 neighbours.  By sampling random convex weights we can
explore a small neighbourhood of configuration space and often reduce the
CKM error far below the value obtained by the simple 2-vertex mixing.

Usage:
    python scripts/optimize_ckm_vevs2.py [--trials N] [--seed S]

Outputs a summary and writes `data/ckm_opt2.json` containing the best pair
and corresponding weights.
"""

from __future__ import annotations

import json
import os
import sys
import argparse
import random

import numpy as np

# make repository imports work
repo_root = os.path.abspath(os.path.join(os.path.dirname(__file__), ".."))
sys.path.insert(0, repo_root)
sys.path.insert(0, os.path.dirname(__file__))

from w33_ckm_from_vev import (
    _build_hodge_and_generations,
    build_generation_profiles,
    cubic_form_on_h27,
    compute_ckm_and_jarlskog,
)
from e8_embedding_group_theoretic import build_w33


# ---------------------------------------------------------------------------
# helpers
# ---------------------------------------------------------------------------

def ckme(v_up, v_dn, X_profiles, local_tris):
    Y_u = np.zeros((3, 3), dtype=complex)
    Y_d = np.zeros((3, 3), dtype=complex)
    for a in range(3):
        for b in range(a, 3):
            Y_u[a, b] = Y_u[b, a] = cubic_form_on_h27(
                None, local_tris, X_profiles[a], X_profiles[b], v_up
            )
            Y_d[a, b] = Y_d[b, a] = cubic_form_on_h27(
                None, local_tris, X_profiles[a], X_profiles[b], v_dn
            )
    V, J = compute_ckm_and_jarlskog(Y_u, Y_d)
    Vexp = np.array([
        [0.97373, 0.2243, 0.00382],
        [0.2210, 0.987, 0.0410],
        [0.0080, 0.0388, 1.013],
    ])
    err = float(np.linalg.norm(np.abs(V) - Vexp))
    return err, V, J


def random_convex(vecs, rng):
    """Return random convex combination of given basis vectors.

    `vecs` is a list of numpy arrays with same length; we return
    
        sum_i w_i * vecs[i]  with w_i >=0, sum w_i = 1
    
    Sampling is done by generating exponential randoms and normalizing.
    """
    weights = rng.exponential(scale=1.0, size=len(vecs))
    weights /= np.sum(weights)
    result = np.zeros_like(vecs[0])
    for w, v in zip(weights, vecs):
        result += w * v
    return result, weights


def main():
    parser = argparse.ArgumentParser(description="Stochastic CKM optimizer")
    parser.add_argument("--trials", type=int, default=1000,
                        help="number of random combinations to try")
    parser.add_argument("--seed", type=int, default=42,
                        help="random seed")
    args = parser.parse_args()

    if not os.path.exists("data/w33_yukawa_blocks.json"):
        print("Run w33_yukawa_blocks.py first")
        sys.exit(1)

    data = json.load(open("data/w33_yukawa_blocks.json"))
    best = data.get("vertex_scan_best")
    if best is None:
        print("No vertex_scan_best entry in JSON; run previous script")
        sys.exit(1)
    vi = best["vi_up"]
    vj = best["vj_down"]

    n, vertices, adj, edges = build_w33()
    v0 = 0
    neighbours_v0 = set(adj[v0])
    H27_idx = [i for i in range(n) if i != v0 and i not in neighbours_v0]
    h27_set = set(H27_idx)
    h27_adj = {i: [] for i in H27_idx}
    for u in H27_idx:
        for w in adj[u]:
            if w in h27_set:
                h27_adj[u].append(w)

    H, triangles, edges, gens = _build_hodge_and_generations()
    H27, local_tris, X_profiles = build_generation_profiles(H, edges, gens, v0=0)

    # construct basis list for up and down: include the original and neighbors
    up_basis_indices = [H27_idx[vi]] + h27_adj[H27_idx[vi]]
    dn_basis_indices = [H27_idx[vj]] + h27_adj[H27_idx[vj]]
    up_vectors = [np.eye(27)[up_basis_indices.index(idx)] for idx in up_basis_indices]
    dn_vectors = [np.eye(27)[dn_basis_indices.index(idx)] for idx in dn_basis_indices]
    # note: we need mapping from H27_idx to position in vector
    index_to_pos = {idx: pos for pos, idx in enumerate(H27_idx)}

    rng = np.random.default_rng(args.seed)

    # base error
    v_up0 = np.zeros(27); v_up0[index_to_pos[H27_idx[vi]]] = 1.0
    v_dn0 = np.zeros(27); v_dn0[index_to_pos[H27_idx[vj]]] = 1.0
    base_err, _, _ = ckme(v_up0, v_dn0, X_profiles, local_tris)
    best_err = base_err
    best_data = {
        "err": base_err,
        "weights_up": None,
        "weights_dn": None,
        "up_indices": up_basis_indices,
        "dn_indices": dn_basis_indices,
    }

    for t in range(args.trials):
        v_up_rand, wu = random_convex(up_vectors, rng)
        v_dn_rand, wd = random_convex(dn_vectors, rng)
        err, V, J = ckme(v_up_rand, v_dn_rand, X_profiles, local_tris)
        if err < best_err:
            best_err = err
            best_data.update({
                "err": err,
                "weights_up": wu.tolist(),
                "weights_dn": wd.tolist(),
            })
            print(f"trial {t}: improved error to {err:.6f}")

    print("base err", base_err, "best err", best_err)
    with open("data/ckm_opt2.json", "w") as f:
        json.dump(best_data, f, indent=2)
    print("written data/ckm_opt2.json")


if __name__ == "__main__":
    main()
