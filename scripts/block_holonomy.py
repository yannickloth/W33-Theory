#!/usr/bin/env python3
"""Compute holonomy (cocycle) of tomotope glue on the 48 incidence blocks.

Outputs:
  - fiber permutations for r1,r2 on each block
  - holonomy group generated by cycles in the base graph
  - list of blocks where the cocycle is nontrivial
  - (optional) correlate those blocks with pocket indices if available
"""
import json, csv, os
from collections import defaultdict
import networkx as nx

# load block partition (list of 48 blocks each with 4 flags)
bundle_dir = 'TOE_block_twist_v02/TOE_tomotope_axis_block_twist_v02_20260228'
blocks = json.load(open(os.path.join(bundle_dir,'blocks48_r0r3.json')))['orbits']
flag2block = {}
for bi,blk in enumerate(blocks):
    for fid in blk:
        flag2block[fid] = bi

# load tomotope generators (axis coords)
tr = json.load(open(os.path.join(bundle_dir,'tomotope_r_generators_in_axis_coords.json')))
r1 = tr['r1']; r2 = tr['r2']

# compute fiber map (destination block + permutation) for r1,r2
fiber_map = { 'r1':{}, 'r2':{} }
block_map = { 'r1':{}, 'r2':{} }
for gen,name in ((r1,'r1'),(r2,'r2')):
    for bi,blk in enumerate(blocks):
        perm = []
        for li,fid in enumerate(blk):
            tgt = gen[fid]
            tgt_block = flag2block[tgt]
            perm.append((tgt_block, blocks[tgt_block].index(tgt)))
        fiber_map[name][bi] = perm
        # block map now ambiguous; choose primary dest for convenience
        block_map[name][bi] = perm[0][0]

# instead compute holonomy by exploring group generated by r1,r2
# begin with identity
idp = tuple(range(192))
elems = {idp}
frontier = [idp]
r1p = tuple(r1); r2p = tuple(r2)
print('building <r1,r2> closure...')
while frontier:
    g = frontier.pop()
    for gen in (r1p,r2p):
        comp = tuple(gen[g[i]] for i in range(192))
        if comp not in elems:
            elems.add(comp); frontier.append(comp)
print('closure size', len(elems))

# compute holonomy groups for each block
holonomy_by_block = {}
for b in range(48):
    base_flags = blocks[b]
    holset = set()
    for g in elems:
        dests = [g[f] for f in base_flags]
        if all(flag2block[d]==b for d in dests):
            perm = tuple(blocks[b].index(d) for d in dests)
            holset.add(perm)
    holonomy_by_block[b] = holset
    print(f'block {b} stabilizer perms', holset)

# global holonomy group (union)
fiber_holonomy = set().union(*holonomy_by_block.values())
print('global holonomy group size', len(fiber_holonomy))
print('global holonomy members', fiber_holonomy)

# compute splitting statistics for each block and generator
split_stats = {'r1':{}, 'r2':{}}
for name,gen in (('r1',r1),('r2',r2)):
    for bi,blk in enumerate(blocks):
        dests = [flag2block[gen[f]] for f in blk]
        counts = {}
        for d in dests: counts[d]=counts.get(d,0)+1
        split_stats[name][bi] = counts
        if len(counts)>1:
            # record splits
            pass

print('example split stats for block0 r1', split_stats['r1'][0])
print('example split stats for block0 r2', split_stats['r2'][0])

# save summary
summary = {
    'holonomy_by_block': {b:list(perms) for b,perms in holonomy_by_block.items()},
    'global_holonomy': list(fiber_holonomy),
    'split_stats': split_stats,
}
json.dump(summary, open('block_holonomy_summary.json','w'), indent=2)
print('summary written to block_holonomy_summary.json')

