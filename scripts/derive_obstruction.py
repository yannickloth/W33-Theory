# helper closure with cutoff

def closure_elems(gen_list, max_elems=10000):
    G={tuple(range(48))}
    changed=True
    while changed:
        changed=False
        for g in list(G):
            for h in gen_list:
                comp=tuple(h[g[i]] for i in range(48))
                if comp not in G:
                    G.add(comp); changed=True
                    if len(G) > max_elems:
                        return None
    return G

