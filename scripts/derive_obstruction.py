# helper to compute orbit size under given generators using BFS

def orbit_size(gen_list, start=0):
    seen=set([start])
    q=deque([start])
    while q:
        v=q.popleft()
        for g in gen_list:
            w=g[v]
            if w not in seen:
                seen.add(w); q.append(w)
    return len(seen)

# compute intersection (approximate) by counting points fixed by both orbit closures

def intersection_orbit_size(gensA, gensB):
    count=0
    for v in range(48):
        if orbit_size(gensA,v)==1 and orbit_size(gensB,v)==1:
            count+=1
    return count

