#!/usr/bin/env python3
"""Generate a Lean file embedding explicit conjugator witnesses found by Python enumeration.

Writes to `proofs/lean/conjugators_generated.lean` by default. The generated theorem is
`GL2F3Enumeration.candidates_conjugate_generated` which mirrors the hand-written proof
and can be used to detect drift between Python enumeration and checked-in Lean fixtures.
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Iterable, Sequence, Tuple

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in __import__("sys").path:
    __import__("sys").path.insert(0, str(ROOT))

from tools.analyze_gl2_f3_involution_conjugacy import build_report


def fmt_tuple(t: Sequence[int]) -> str:
    return ", ".join(str(int(x)) for x in t)


def mk_case_eq(m: Tuple[int, int, int, int]) -> str:
    a, b, c, d = m
    return f"M = mk2x2 {a} {b} {c} {d}"


