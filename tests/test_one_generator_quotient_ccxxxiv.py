# T1 -- The Quotient Ring
# ===========================================================================
class TestT1_QuotientRing:
    """Z[lam]/(lam^2 - lam - 2) has unique positive integer root lam=2."""

    def test_quadratic_relation(self):
        """lam^2 - lam - 2 = 0 at lam=2."""
        assert lam**2 - lam - 2 == 0

    def test_factors(self):
        """lam^2 - lam - 2 = (lam-2)(lam+1)."""
        L = Symbol('lam')
        poly = L**2 - L - 2
        f = factor(poly)
        assert f == (L - 2) * (L + 1)

    def test_unique_positive_root(self):
        """Positive integer root is unique: lam=2."""
        roots = [l for l in range(1, 100) if l**2 - l - 2 == 0]
        assert roots == [2]

    def test_family_law_mu_lam_plus_2(self):
        """Family law: mu = q+1 = (lam+1)+1 = lam+2."""
        assert mu == lam + 2

    def test_selector_mu_lam_squared(self):
        """Selector: mu = lam^2."""
        assert mu == lam**2

    def test_combined(self):
        """lam+2 = lam^2 => lam^2-lam-2 = 0."""
        assert lam + 2 == lam**2


