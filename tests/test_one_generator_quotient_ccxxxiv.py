class TestT4_HProjector:
    """H_t = -(lam/2)*(1 + rho^{t+1}) where rho=4."""

    def _H(self, t):
        """H_t = O_t - (k/lam)*E_t. Using closed form: -(lam/2)*(1+4^{t+1})."""
        return -Fraction(lam, 2) * (1 + 4**(t + 1))

    def test_H0(self):
        """-H_0 = 5 = Phi2(mu)."""
        assert -self._H(0) == 5

    def test_H1(self):
        """-H_1 = 17 = Phi4(mu)."""
        assert -self._H(1) == 17

    def test_H2(self):
        """-H_2 = 65 = 5*13."""
        assert -self._H(2) == 65
        assert 65 == 5 * 13

    def test_H3(self):
        """-H_3 = 257 (Fermat prime F_3)."""
        assert -self._H(3) == 257

    def test_H4(self):
        """-H_4 = 1025 = 5^2*41."""
        assert -self._H(4) == 1025
        assert 1025 == 5**2 * 41

    def test_general_formula(self):
        """-H_t = 1 + 4^{t+1} = 1 + mu^{t+1}."""
        for t in range(6):
            assert -self._H(t) == 1 + mu**(t + 1)


