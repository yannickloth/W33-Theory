# T5 -- Odd-Core Recurrence Coefficients
# ===========================================================================
class TestT5_OddCoreRecurrence:
    """Odd-core tower satisfies O_{t+3} = S1*O_{t+2} - S2*O_{t+1} + S3*O_t."""

    def test_S1(self):
        """S1 = (q^4+2q^3+3q^2+2)/(q-1)^2 = 41 = v+1 at q=3."""
        S1 = (q**4 + 2*q**3 + 3*q**2 + 2) // (q - 1)**2
        assert S1 == 41 == v + 1

    def test_S2(self):
        """S2 = 184 = 8*23 = (k-mu)*(f-1) at q=3."""
        r_c = k - mu  # compact rank = 8
        assert r_c * (f - 1) == 184

    def test_S3(self):
        """S3 = q^2*(q+1)^4/(q-1)^4 = 9*256/16 = 144 = k^2 at q=3."""
        S3 = q**2 * (q + 1)**4 // (q - 1)**4
        assert S3 == 144 == k**2

    def test_S1_selector(self):
        """S1 = v+1 has (q-3) factor in the residual."""
        for qq in range(3, 30):
            num = qq**4 + 2*qq**3 + 3*qq**2 + 2
            den = (qq - 1)**2
            vv = (qq + 1) * (qq**2 + 1)
            if num % den == 0 and num // den == vv + 1:
                assert qq == 3
