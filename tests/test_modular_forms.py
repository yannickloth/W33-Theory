    """Genus of X_0(N) and X_1(N) at SRG-relevant levels."""

    def test_genus_x0_1(self):
        """g(X_0(1)) = 0. Level 1 is trivial."""
        assert 0 == 0

    def test_genus_x0_11(self):
        """g(X_0(11)) = 1. First level with genus 1."""
        # 11 is the smallest N with g > 0
        assert 11 > THETA

