    """B_0 = 1, B_1 = -1/2, B_2 = 1/6, B_4 = -1/30, B_6 = 1/42, ...
    Key: denominators involve SRG-related primes."""

    def test_b2_denominator(self):
        """B_2 = 1/6. Denominator 6 = |r - s| = 2*q."""
        assert abs(R - S) == 6

    def test_b4_denominator(self):
        """B_4 = -1/30. Denominator 30 = |r-s| * N = h(E8)."""
        assert abs(R - S) * N == 30

