# T486: E8 Theta Series
# ──────────────────────────────────────────────
class TestE8ThetaSeries:
    """The E8 lattice theta series: Theta_E8(q) = 1 + 240q + 2160q^2 + ...
    The coefficient of q^1 is 240 = E (kissing number)."""

    def test_theta_coeff_1(self):
        """Coefficient of q^1 in Theta_E8 = 240 = E."""
        assert E == 240

    def test_theta_coeff_2(self):
        """Coefficient of q^2 in Theta_E8 = 2160 = E * q^2."""
        assert E * Q**2 == 2160

    def test_theta_coeff_ratio(self):
        """a(2)/a(1) = 2160/240 = 9 = q^2."""
        assert 2160 // 240 == Q**2

    def test_theta_as_eisenstein(self):
        """Theta_E8 = E_4 (Eisenstein series of weight 4).
        E_4 = 1 + 240*sum(sigma_3(n)*q^n)."""
        assert E == 240

    def test_sigma3_1(self):
        """sigma_3(1) = 1. So a(1) = 240 * 1 = E."""
        sigma3_1 = 1
        assert E * sigma3_1 == 240


