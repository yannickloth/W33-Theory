            ss = -(qq + 1)
            ff = qq * (qq + 1)**2 // 2
            gg = qq * (qq**2 + 1) // 2
            vv = (qq + 1) * (qq**2 + 1)
            pp6 = qq**2 - qq + 1
            M3 = kk**3 + ff * rr**3 + gg * ss**3
