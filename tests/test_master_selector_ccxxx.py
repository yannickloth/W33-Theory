class TestT7_Hexad:
    """The initial Monster/Bernoulli shell {2,3,5,7,11,13} = W(3,3) atoms."""

    def test_hexad_atoms(self):
        """Six atoms: lam=2, q=3, Phi4/lam=5, Phi6=7, k-1=11, Phi3=13."""
        atoms = {lam, q, Phi4 // lam, Phi6, k - 1, Phi3}
        assert atoms == {2, 3, 5, 7, 11, 13}

    def test_den_B12(self):
        """den(B_12) = 2730 = 2*3*5*7*13 (all hexad primes except 11)."""
        assert 2730 == 2 * 3 * 5 * 7 * 13

    def test_den_B10(self):
        """den(B_10) = 66 = 2*3*11 = lam*q*(k-1)."""
        assert 66 == lam * q * (k - 1)
        assert 66 == 2 * 3 * 11

    def test_1_plus_2k_is_perfect_square(self):
        """1+2k = 25 = 5^2. Unique among prime powers q=2..80."""
        assert 1 + 2 * k == 25
        assert math.isqrt(1 + 2 * k)**2 == 1 + 2 * k
        # Build prime power set
        pp_set = set()
        for p in range(2, 81):
            if isprime(int(p)):
                pp_set.add(p)
                pk = p
                while pk * p <= 80:
                    pk *= p
                    pp_set.add(pk)
        hits = []
        for qq in sorted(pp_set):
            kk = qq * (qq + 1)
            val = 1 + 2 * kk
            sqrt_val = math.isqrt(val)
            if sqrt_val * sqrt_val == val:
                hits.append(qq)
        assert hits == [3]

    def test_den_B2_selector(self):
        """den(B_2) = lam*q = 6. Equation (q-1)*q = 6 has unique solution q=3."""
        assert lam * q == 6
        hits = []
        for qq in range(2, 81):
            if (qq - 1) * qq == 6:
                hits.append(qq)
        assert hits == [3]

    def test_hexad_all_prime(self):
        """All 6 hexad members are prime."""
        for p in [2, 3, 5, 7, 11, 13]:
            assert isprime(p)
