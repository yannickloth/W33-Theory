class TestT3_ReductionRule:
    """λ² = λ + 2 is the only reduction needed."""

    def test_lam_squared(self):
        assert lam**2 == lam + 2

    def test_lam_cubed(self):
        """λ³ = λ·λ² = λ(λ+2) = λ²+2λ = (λ+2)+2λ = 3λ+2."""
        assert lam**3 == 3*lam + 2

