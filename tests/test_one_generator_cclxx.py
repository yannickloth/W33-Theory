class TestT4_CrossVerification:
    """Linearized values match the standard W(3,3) parameters."""

    def test_v(self):
        """v = (q+1)(q²+1) = μ·Φ₄ = (λ+2)(3λ+4) = ...evaluate..."""
        q = lam + 1
        v = (q + 1) * (q**2 + 1)
        assert v == 40

    def test_k_from_q_mu(self):
        q = lam + 1
        mu = lam + 2
        assert q * mu == 12

    def test_alpha_from_atoms(self):
        k = 4 * lam + 4
        mu = lam + 2
        assert (k - 1)**2 + mu**2 == 137

