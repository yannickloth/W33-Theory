import pytest

# ── The quotient ──
lam = 2   # unique positive root of λ²−λ−2=0


def _lin(a, b):
    """Evaluate a + b·λ in the quotient."""
    return a + b * lam


# ================================================================
# T1: The selector equation
# ================================================================
class TestT1_SelectorEquation:
    """λ²−λ−2 = 0 with unique positive root λ=2."""

    def test_equation_satisfied(self):
        assert lam**2 - lam - 2 == 0

    def test_factored(self):
        assert (lam - 2) * (lam + 1) == 0

    def test_unique_positive(self):
        """Only positive integer root is 2."""
        for x in range(1, 20):
            if x**2 - x - 2 == 0:
                assert x == 2

    def test_from_mu_equals_lam2(self):
        """μ=λ² and μ=λ+2 give λ²−λ−2=0."""
        # μ = λ² and μ = λ+2 ⟹ λ²=λ+2 ⟹ λ²−λ−2=0
        assert lam**2 == lam + 2


