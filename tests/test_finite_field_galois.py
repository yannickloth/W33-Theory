# T469: Weil Bound for Curves
# ──────────────────────────────────────────────
class TestWeilBound:
    """For a curve of genus g over GF(q):
    |N - (q+1)| <= 2g*sqrt(q).
    For elliptic curve (g=1): |N - 4| <= 2*sqrt(3) ~ 3.46."""

    def test_weil_elliptic(self):
        """For elliptic curve over GF(3): |N-4| <= 3.46."""
        bound = 2 * math.sqrt(Q)
        assert abs(bound - 2 * math.sqrt(3)) < 1e-10
        # N can range from 1 to 7

    def test_weil_genus_2(self):
        """For genus-2 curve: |N-(q+1)| <= 4*sqrt(q) ~ 6.93."""
        g_curve = 2
        bound = 2 * g_curve * math.sqrt(Q)
        assert abs(bound - 4 * math.sqrt(3)) < 1e-10

    def test_maximal_curve(self):
        """Maximal curve: N = q + 1 + 2g*sqrt(q).
        For g=1 over GF(9): N = 10+6 = 16 or with floor: N <= 9+1+6 = 16."""
        # Over GF(q^2) for q=3: maximal N = q^2+1+2*1*q = 9+1+6 = 16
        N_max = Q**2 + 1 + 2 * Q
        assert N_max == 16
        assert N_max == (Q + 1)**2

    def test_hasse_interval(self):
        """For E/GF(3): N in [1, 7]. q+1-2sqrt(q) ~ 0.54, q+1+2sqrt(q) ~ 7.46."""
        lo = Q + 1 - 2 * math.sqrt(Q)
        hi = Q + 1 + 2 * math.sqrt(Q)
        assert lo > 0
        assert hi < 8
        # Possible N: 1,2,3,4,5,6,7

    def test_trace_of_frobenius(self):
        """a_p = q+1-N. For supersingular: a_p = 0, N = q+1 = mu."""
        N_ss = Q + 1
        assert N_ss == MU


