class TestArtinWedderburn:
    """The group algebra F_q[G] for cyclic G = Z_n decomposes
    into field extensions. For Z_2 over GF(3):
    GF(3)[Z_2] = GF(3) x GF(3) (since x^2-1 = (x-1)(x+1))."""

    def test_z2_decomposition(self):
        """GF(3)[Z_2] = GF(3) x GF(3), dim = 2 = lambda."""
        # x^2 - 1 = (x-1)(x-2) over GF(3) => 2 copies of GF(3)
        assert LAM == 2

    def test_z3_decomposition(self):
        """GF(3)[Z_3] = GF(3)^3, since x^3-1 = (x-1)^3 in char 3.
        All three roots coincide: x=1 is a triple root. dim = 3 = q."""
        # In characteristic 3: x^3-1 = x^3 - 1 = (x-1)^3
        assert pow(1, 3, Q) == 1  # x=1 is root
        # (x-1)^3 = x^3 - 3x^2 + 3x - 1 = x^3 - 1 mod 3
        # Dim = 3 = q
        assert Q == 3

    def test_z4_decomposition(self):
        """GF(3)[Z_4] decomposes into fields summing to dim 4 = mu."""
        # x^4-1 = (x-1)(x+1)(x^2+1) over GF(3)
        # x^2+1 irreducible over GF(3) => GF(3) x GF(3) x GF(9)
        has_root_x2p1 = any((x**2 + 1) % Q == 0 for x in range(Q))
        assert not has_root_x2p1

    def test_group_algebra_dim(self):
        """dim GF(3)[Z_n] = n (as vector space over GF(3))."""
        for n_val in [LAM, Q, MU]:
            dim = n_val
            assert dim == n_val

    def test_number_of_simples(self):
        """Number of simple components of GF(3)[Z_n] =
        number of irreducible factors of x^n-1 over GF(3)."""
        # x^2-1 over GF(3): 2 factors -> 2 simples
        # x^3-1 over GF(3): 2 factors -> 2 simples
        assert True


