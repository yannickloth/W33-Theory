# T462: Irreducible Polynomial Counts
# ──────────────────────────────────────────────
class TestIrreduciblePolynomials:
    """N_q(n) = number of monic irreducible polynomials of degree n over GF(q).
    N_q(n) = (1/n) * sum_{d|n} mu(n/d) * q^d (necklace formula)."""

    def _irred_count(self, n):
        """Count irreducible polys of degree n over GF(q)."""
        from math import gcd
        def mobius(m):
            if m == 1:
                return 1
            factors = 0
            temp = m
            p = 2
            while p * p <= temp:
                if temp % p == 0:
                    factors += 1
                    temp //= p
                    if temp % p == 0:
                        return 0
                p += 1
            if temp > 1:
                factors += 1
            return (-1) ** factors

        total = 0
        for d in range(1, n + 1):
            if n % d == 0:
                total += mobius(n // d) * Q**d
        return total // n

    def test_irred_degree_1(self):
        """N_3(1) = 3 = q (x, x-1, x-2 = x+1)."""
        assert self._irred_count(1) == Q

    def test_irred_degree_2(self):
        """N_3(2) = (9-3)/2 = 3 = q."""
        assert self._irred_count(2) == Q

    def test_irred_degree_3(self):
        """N_3(3) = (27-3)/3 = 8 = 2^3."""
        assert self._irred_count(3) == 8
        assert 8 == 2**3

    def test_irred_degree_4(self):
        """N_3(4) = (81-9)/4 = 18 = 2*q^2."""
        assert self._irred_count(4) == 18
        assert 18 == 2 * Q**2

    def test_irred_degree_6(self):
        """N_3(6)."""
        n6 = self._irred_count(6)
        # (729-27-9+3)/6 = 696/6... let me compute properly
        # sum = mu(1)*3^6 + mu(2)*3^3 + mu(3)*3^2 + mu(6)*3
        # = 729 - 27 - 9 + 3 = 696; 696/6 = 116
        assert n6 == 116


