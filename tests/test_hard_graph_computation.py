        L = 12 * np.eye(40) - w33["adj"].astype(float)
        evals = np.linalg.eigvalsh(L)
        deriv2 = sum(e**2 for e in evals)
        # Tr(L^2) = Tr((kI-A)^2) = k^2*v - 2k*Tr(A) + Tr(A^2)
        # = 144*40 - 0 + 480 = 6240
        assert abs(deriv2 - 6240) < 1e-6


