# T904: Alpha Uniqueness — Stress Test Against Other SRGs
# ═══════════════════════════════════════════════════════════════════════
class TestT904AlphaUniqueness:
    """The same formula applied to other known SRGs gives nonsense."""

    def _alpha_formula(self, v, k, lam, mu):
        L_eff = (k - 1) * ((k - lam)**2 + 1)
        if L_eff == 0:
            return None
        return k**2 - 2*mu + 1 + v / L_eff

    def test_paley13_gives_wrong(self):
        """Paley(13): SRG(13,6,2,3) -> alpha_inv = 31.something."""
        val = self._alpha_formula(13, 6, 2, 3)
        assert abs(val - 137) > 10  # nowhere near 137

    def test_petersen_gives_wrong(self):
        """Petersen graph: SRG(10,3,0,1) -> alpha_inv far from 137."""
        val = self._alpha_formula(10, 3, 0, 1)
        assert val is not None
        assert abs(val - 137) > 10

    def test_srg16_6_2_2_gives_wrong(self):
        """SRG(16,6,2,2) -> alpha_inv != 137."""
        val = self._alpha_formula(16, 6, 2, 2)
        assert abs(val - 137) > 10

    def test_hoffman_singleton_gives_wrong(self):
        """Hoffman-Singleton: SRG(50,7,0,1) -> not 137."""
        val = self._alpha_formula(50, 7, 0, 1)
        assert abs(val - 137) > 10

    def test_w33_is_unique(self):
        """Only W(3,3) = SRG(40,12,2,4) gives ~137.036."""
        val = self._alpha_formula(40, 12, 2, 4)
        assert abs(val - 137.036) < 0.001


