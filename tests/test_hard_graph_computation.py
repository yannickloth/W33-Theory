# T895: Ihara-Bass Determinant Identity
# ═══════════════════════════════════════════════════════════════════════
class TestT895IharaBass:
    """det(I - uB) = (1-u^2)^{m-n} * det(I - uA + u^2(k-1)I) at 6 probes."""

    @pytest.fixture(scope="class")
    def ihara_data(self, w33, hashimoto):
        B_dense = hashimoto["B"].toarray()
        A_float = w33["adj"].astype(float)
        n, m, k = 40, 240, 12
        I_n = np.eye(n)
        I_2m = np.eye(480)
        return {
            "B": B_dense, "A": A_float, "n": n, "m": m, "k": k,
            "I_n": I_n, "I_2m": I_2m
        }

    @pytest.mark.parametrize("u", [0.01, 0.03, 0.05, 0.07, 0.08, 0.10])
    def test_ihara_bass_at_u(self, ihara_data, u):
        d = ihara_data
        # LHS: log|det(I - uB)|
        _, lhs_log = np.linalg.slogdet(d["I_2m"] - u * d["B"])
        # RHS: (m-n)*log|1-u^2| + log|det(I - uA + u^2*(k-1)*I)|
        rhs_factor = (d["m"] - d["n"]) * np.log(abs(1 - u**2))
        _, rhs_log = np.linalg.slogdet(
            d["I_n"] - u * d["A"] + u**2 * (d["k"] - 1) * d["I_n"]
        )
        total_rhs = rhs_factor + rhs_log
        assert abs(lhs_log - total_rhs) < 1e-8, \
            f"Ihara-Bass fails at u={u}: diff={abs(lhs_log - total_rhs):.2e}"


