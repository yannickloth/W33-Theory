# T896: Vertex Propagator M — Spectrum and Green's Function
# ═══════════════════════════════════════════════════════════════════════
class TestT896VertexPropagator:
    """M = (k-1)*((A-lambda*I)^2 + I); eigenvalues {1111,11,407}."""

    def test_ones_eigenvector(self, w33, propagator):
        ones = np.ones(40)
        M_ones = propagator @ ones
        assert np.allclose(M_ones, 1111 * ones)

    def test_m_eigenval_from_k(self, propagator):
        evals = sorted(np.linalg.eigvalsh(propagator))
        counts = Counter(round(e) for e in evals)
        assert counts[1111] == 1

    def test_m_eigenval_from_r(self, propagator):
        evals = np.linalg.eigvalsh(propagator)
        counts = Counter(round(e) for e in evals)
        # A eigenvalue 2: M = 11*((2-2)^2 + 1) = 11
        assert counts[11] == 24

    def test_m_eigenval_from_s(self, propagator):
        evals = np.linalg.eigvalsh(propagator)
        counts = Counter(round(e) for e in evals)
        # A eigenvalue -4: M = 11*((-4-2)^2 + 1) = 11*37 = 407
        assert counts[407] == 15

    def test_greens_function(self, propagator):
        """1^T M^{-1} 1 = v / eigenvalue(1) = 40/1111."""
        ones = np.ones(40)
        M_inv_ones = np.linalg.solve(propagator, ones)
        qf = ones @ M_inv_ones
        expected = Fraction(40, 1111)
        assert abs(qf - float(expected)) < 1e-12

    def test_m_positive_definite(self, propagator):
        """M is positive definite (all eigenvalues > 0)."""
        evals = np.linalg.eigvalsh(propagator)
        assert all(e > 0 for e in evals)

    def test_m_trace(self, propagator):
        """Tr(M) = 1*1111 + 24*11 + 15*407."""
        expected = 1111 + 24*11 + 15*407
        assert abs(np.trace(propagator) - expected) < 1e-6

    def test_m_inv_trace(self, propagator):
        """Tr(M^{-1}) = 1/1111 + 24/11 + 15/407."""
        M_inv = np.linalg.inv(propagator)
        expected = 1/1111 + 24/11 + 15/407
        assert abs(np.trace(M_inv) - expected) < 1e-10


