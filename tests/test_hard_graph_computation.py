def _canonical(v, q=3):
    """Projective canonical representative: first nonzero entry normalised to 1."""
    for i in range(len(v)):
        if v[i] % q != 0:
            inv = pow(int(v[i]), -1, q)
            return tuple((int(c) * inv) % q for c in v)
    return None

def _build_w33():
    """Build W(3,3) = Sp(4,3) from the symplectic form on GF(3)^4."""
    q = 3
    raw = [v for v in iproduct(range(q), repeat=4) if any(x != 0 for x in v)]
    seen = set()
    vertices = []
    for v in raw:
        c = _canonical(v, q)
        if c is not None and c not in seen:
            seen.add(c)
            vertices.append(c)
    vertices.sort()
    n = len(vertices)

    # Symplectic form: omega(x,y) = x0*y2 - x2*y0 + x1*y3 - x3*y1 (mod 3)
    def omega(x, y):
        return (x[0]*y[2] - x[2]*y[0] + x[1]*y[3] - x[3]*y[1]) % q

    adj = np.zeros((n, n), dtype=np.int8)
    edges = []
    for i in range(n):
        for j in range(i+1, n):
            if omega(vertices[i], vertices[j]) == 0:
                adj[i, j] = adj[j, i] = 1
                edges.append((i, j))
    return n, vertices, adj, edges

def _build_complement(adj):
    """Build the complement graph adjacency matrix."""
    n = adj.shape[0]
    comp = np.ones((n, n), dtype=np.int8) - adj
    np.fill_diagonal(comp, 0)
    return comp

def _build_nonbacktracking(adj, edges, n):
    """Build the 480×480 non-backtracking (Hashimoto) operator."""
    directed = []
    for i, j in edges:
        directed.append((i, j))
        directed.append((j, i))
    de_idx = {e: k for k, e in enumerate(directed)}
    nd = len(directed)
    rows, cols = [], []
    for idx_ab, (a, b) in enumerate(directed):
        for c in range(n):
            if c != a and adj[b, c] == 1:
                rows.append(idx_ab)
                cols.append(de_idx[(b, c)])
    from scipy import sparse
    B = sparse.csr_matrix(
        (np.ones(len(rows), dtype=float), (rows, cols)),
        shape=(nd, nd)
    )
    return B, directed

def _symplectic_form_matrix(q=3):
    """Standard 4×4 symplectic form [[0, I2], [-I2, 0]] over GF(q)."""
    I2 = np.eye(2, dtype=int)
    Z2 = np.zeros((2, 2), dtype=int)
    J = np.block([[Z2, I2], [(-I2) % q, Z2]]) % q
    return J

def _transvection_matrix(v, J, q=3):
    """Symplectic transvection matrix: x -> x + omega(x,v)*v."""
    v = np.array(v, dtype=int) % q
    M = (np.eye(4, dtype=int) + (v.reshape(4,1) @ (v.reshape(1,4) @ J)) % q) % q
    return M

def _matrix_to_perm(M, points, idx_map, q=3):
    """Convert a 4×4 matrix to a permutation on projective points."""
    perm = []
    for p in points:
        w = (M @ np.array(p, dtype=int)) % q
        rep = _canonical(w, q)
        perm.append(idx_map[rep])
    return tuple(perm)

def _compose_perm(a, b):
    return tuple(b[a[i]] for i in range(len(a)))


