#!/usr/bin/env python3
"""Compute the lift subgroup of the canonical edge->root bijection φ.

Find all g in Aut(W33) (generated by the six PSp(4,3) permutations) such
that the induced permutation on the 240 E8 roots is an isometry of the E8
inner product.  The output JSON records the subgroup order and the list of
vertex permutations (as lists of length 40).  A separate text file lists the
240-edge permutation of the outer twist so that we can check non-membership.

Outputs written to artifacts/phi_lift_subgroup.json and
artifacts/outer_twist_permutation.json.
"""
from __future__ import annotations

import json, zipfile
from pathlib import Path
from collections import deque
import numpy as np
import networkx as nx

ROOT = Path(__file__).resolve().parents[1]
import sys
# add repo root and scripts folder to import path
sys.path.insert(0, str(ROOT))
sys.path.insert(0, str(ROOT / 'scripts'))

# bundle locating helper (reuse earlier function pattern)
def find_bundle(pattern: str) -> Path:
    p = ROOT / f"{pattern}.zip"
    if p.exists():
        return p
    p2 = ROOT / pattern
    if p2.exists():
        return p2
    cands = list(ROOT.glob(pattern + "*"))
    if cands:
        return cands[0]
    raise FileNotFoundError(pattern)

def load_json_from_bundle(bundle: Path, name: str):
    if bundle.is_file():
        with zipfile.ZipFile(bundle) as z:
            return json.loads(z.read(name))
    else:
        return json.loads((bundle / name).read_text())

CONJ = find_bundle("WE6_EVEN_to_PSp43_CONJUGACY_BUNDLE_v01")
OUTER = find_bundle("H27_OUTER_TWIST_ACTION_BUNDLE_v01")

# build the canonical W33 graph and automorphism group
from scripts.e8_embedding_group_theoretic import build_w33
from scripts.w33_full_decomposition import build_psp43_group

n, vertices, adj_list, edges = build_w33()
# the adjacency labeling used here corresponds to the graph used for 'edges'
# build the full automorphism group as adjacency permutations
group_dict = build_psp43_group(vertices, edges)
Gperms = list(group_dict.keys())
print("Aut(W33) size", len(Gperms))
# Gperms is list of tuples length 40 representing adjacency-label perms

# load edge->root map and build index
edge_to_root = json.loads((ROOT / "artifacts" / "edge_to_e8_root.json").read_text())
# canonicalize and store sorted edges list
edges = []
root_vecs = []
for k,v in edge_to_root.items():
    i,j = [int(x.strip()) for x in k.strip()[1:-1].split(",")]
    if i<j:
        edges.append((i,j))
        root_vecs.append(tuple(int(x) for x in v))
# ensure 240 edges
assert len(edges) == 240
edge_index = {e:i for i,e in enumerate(edges)}

# compute Gram matrix of raw dot products
# (E8 root inner product proportional to usual dot)
def dot(u,v): return sum(int(a)*int(b) for a,b in zip(u,v))

roots = root_vecs
Gram = np.zeros((240,240),dtype=int)
for a in range(240):
    for b in range(240):
        Gram[a,b] = dot(roots[a], roots[b])

# helper to test isometry including orientation sign

def is_isometry(perm, signs, root_list, Gram_mat):
    # perm: edge permutation indices; signs: +1/-1 per edge
    m = len(root_list)
    # build new root list with orientation signs applied
    for a in range(m):
        for b in range(m):
            ra = root_list[perm[a]]
            if signs[a] < 0:
                ra = tuple(-x for x in ra)
            rb = root_list[perm[b]]
            if signs[b] < 0:
                rb = tuple(-x for x in rb)
            if dot(ra, rb) != Gram_mat[a, b]:
                return False
    return True

# compute mapping from adjacency labels to root labels via graph iso
G_adj = nx.Graph()
G_adj.add_nodes_from(range(40))
for i, nbrs in enumerate(adj_list):
    for j in nbrs:
        if i < j:
            G_adj.add_edge(i, j)
G_root = nx.Graph()
G_root.add_nodes_from(range(40))
for i,j in edges:
    if i < j:
        G_root.add_edge(i, j)

gm2 = nx.algorithms.isomorphism.GraphMatcher(G_adj, G_root)
label_map = next(gm2.isomorphisms_iter())
label_map_inv = {v:k for k,v in label_map.items()}
print('computed label_map sample', {k:label_map[k] for k in range(5)})

# general helper to compute lift size for arbitrary root list
# returns either int or (int,list) if return_perms True

def compute_lift_for_roots(root_list: List[Tuple[int, ...]], return_perms: bool=False):
    # build Gram matrix for this root list
    m = len(root_list)
    Gram_local = np.zeros((m,m), dtype=int)
    for a in range(m):
        for b in range(m):
            Gram_local[a,b] = dot(root_list[a], root_list[b])
    count = 0
    lifts = []
    for g in Gperms:
        # convert to root mapping
        g_root = [label_map[g[label_map_inv[i]]] for i in range(40)]
        perm = []
        signs = []
        for i,j in edges:
            ni,nj = g_root[i], g_root[j]
            if ni < nj:
                perm.append(edge_index[(ni,nj)])
                signs.append(1)
            else:
                perm.append(edge_index[(nj,ni)])
                signs.append(-1)
        if is_isometry(perm, signs, root_list, Gram_local):
            count += 1
            lifts.append(list(g))
    if return_perms:
        return count, lifts
    return count

# compute using the canonical mapping stored in 'roots' variable
canonical_lift_size, lift = compute_lift_for_roots(roots, return_perms=True)
print('canonical lift size', canonical_lift_size)
print('stored {} lifting elements'.format(len(lift)))

# also compute permutation for outer twist (p_label) for reference
perm40 = load_json_from_bundle(OUTER, "perm40_and_H27_pg_ids.json")["perm40_points_from_phi_n"]
psi = {int(k):int(v) for k,v in json.loads((ROOT/"pg_to_edge_labeling.json").read_text()).items()}
psi_inv = {v:k for k,v in psi.items()}
p_label = [None]*40
for i in range(40):
    pg = psi_inv[i]
    p_label[i] = psi[perm40[pg]]

# compute induced root permutation for outer twist
outer_perm = []
for e in edges:
    i,j=e
    ni,nj = p_label[i], p_label[j]
    if ni>nj: ni,nj=nj,ni
    outer_perm.append(edge_index[(ni,nj)])

# write certificate
out = {
    "lift_size": canonical_lift_size,
    "lift_generators": lift,
    "outer_perm": outer_perm,
}
(ROOT/"artifacts"/"phi_lift_subgroup.json").write_text(json.dumps(out,indent=2))
print("wrote phi_lift_subgroup.json")
