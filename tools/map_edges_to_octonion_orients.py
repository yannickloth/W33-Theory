#!/usr/bin/env python3
"""Map W33 edges to octonion orientation indices (one of 480).

Uses the edge->oriented-rootpair mapping bundle to recover the 7-pocket
containing each edge.  Then picks a canonical orientation (perm+sign) from
the 480 generated by :func:`generate_orientations` that agrees with the full
pocket multiplication table.  The map has size 240 (edges) and by adding the
global sign flip we obtain 480 distinct orientation classes.

Usage:
    python tools/map_edges_to_octonion_orients.py \
        --tri_zip path/to/triangle_bundle.zip \
        --edge_zip path/to/edge_bundle.zip \
        [--out json]

Outputs a JSON of form:
    {
      "edge_to_orient": [idx0, idx1, ...],   # 240 entries
      "orientations": [[perm, signs], ...],  # 480 orientations list
    }

"""
