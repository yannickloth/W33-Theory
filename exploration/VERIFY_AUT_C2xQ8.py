def q8_mul(a, b):
    """Multiply two Q₈ elements (as integers 0..7)."""
    # Build table from quaternion rules
    # i² = j² = k² = ijk = -1
    table = {}
    # Using (sign, unit) representation: each element = sign * unit
    # units: 1, i, j, k  (indices 0,1,2,3)
    # sign: +1 or -1
    
    def to_pair(x):
        """Convert 0..7 to (sign, unit_index)"""
        # 0=+1, 1=-1, 2=+i, 3=-i, 4=+j, 5=-j, 6=+k, 7=-k
        signs = [1, -1, 1, -1, 1, -1, 1, -1]
        units = [0, 0, 1, 1, 2, 2, 3, 3]
        return signs[x], units[x]
    
    def from_pair(s, u):
        """Convert (sign, unit_index) back to 0..7"""
        base = u * 2
        return base if s == 1 else base + 1
    
    s_a, u_a = to_pair(a)
    s_b, u_b = to_pair(b)
    
    # Unit multiplication table: (sign, result_unit) for unit_a * unit_b
    # 1*x = x, x*1 = x
    # i*i = -1, j*j = -1, k*k = -1
    # i*j = k, j*k = i, k*i = j
    # j*i = -k, k*j = -i, i*k = -j
    unit_mul = {
        (0,0): (1,0), (0,1): (1,1), (0,2): (1,2), (0,3): (1,3),
        (1,0): (1,1), (2,0): (1,2), (3,0): (1,3),
        (1,1): (-1,0), (2,2): (-1,0), (3,3): (-1,0),
        (1,2): (1,3),  (2,3): (1,1),  (3,1): (1,2),
        (2,1): (-1,3), (3,2): (-1,1), (1,3): (-1,2),
    }
    
    s_prod, u_prod = unit_mul[(u_a, u_b)]
    return from_pair(s_a * s_b * s_prod, u_prod)

