def build_automorphism(img_a, img_i, img_j):
    """Build full automorphism from generator images.
    
    Every element of G = C₂ × Q₈ can be written as a^ε * w
    where ε ∈ {0,1} and w is a word in i, j.
    
    We compute φ(g) for every g by expressing g in terms of generators
    and applying φ.
    """
    # Build the map by evaluating φ on each element
    # First, express each element as a product of generators
    # Use BFS from identity
    phi = [None] * 16
    phi[0] = 0  # φ(identity) = identity
    
    # Map generators
    phi[gen_a] = img_a
    phi[gen_i] = img_i
    phi[gen_j] = img_j
    
    # For each unmapped element, try to express it as g1 * g2 where g1, g2 are mapped
    changed = True
    while changed:
        changed = False
        for x in range(16):
            if phi[x] is not None:
                continue
            # Try x = g1 * g2
            for g1 in range(16):
                if phi[g1] is None:
                    continue
                for g2 in range(16):
                    if phi[g2] is None:
                        continue
                    if G_table[g1][g2] == x:
                        candidate = G_table[phi[g1]][phi[g2]]
                        if phi[x] is None:
                            phi[x] = candidate
                            changed = True
                        else:
                            assert phi[x] == candidate, "Inconsistent automorphism!"
                        break
                if phi[x] is not None:
                    break
    
    assert None not in phi, f"Could not map all elements: {phi}"
    assert len(set(phi)) == 16, "Not a bijection!"
    
    # Verify it's a homomorphism
    for x in range(16):
        for y in range(16):
            assert phi[G_table[x][y]] == G_table[phi[x]][phi[y]], \
                f"Not a homomorphism at ({x},{y})"
    
    return tuple(phi)

