# ── Compute the lower central series ──────────────────────────────────
print("\n=== LOWER CENTRAL SERIES ===")
# γ₁(N) = N, γ_{i+1}(N) = [γᵢ(N), N]

def close_subgroup(generators, ambient):
    """Close a set of generators under multiplication within ambient."""
    sub = set(generators)
    sub.add(e)
    gens = list(generators)
    changed = True
    while changed:
        changed = False
        to_add = set()
        for a in list(sub):
            for b in gens:
                p1 = tuple(compose(list(a), list(b)))
                p2 = tuple(compose(list(b), list(a)))
                for p in [p1, p2]:
                    if p in ambient and p not in sub:
                        to_add.add(p)
        if to_add:
            sub |= to_add
            gens = list(to_add)  # only multiply new elements
            changed = True
    return sub

def commutator_subgroup(H, K, ambient):
    """Compute [H, K] = subgroup generated by [h,k] for h∈H, k∈K."""
    comms = set()
    for h in H:
        for k in K:
            c = tuple(compose(compose(list(h), list(k)),
                             compose(inv(list(h)), inv(list(k)))))
            if c in ambient:
                comms.add(c)
    return close_subgroup(comms, ambient)

gamma = [None, N_set]  # γ₁ = N
for i in range(2, 8):
    gi = commutator_subgroup(gamma[i-1], N_set, N_set)
    gamma.append(gi)
    print(f"  γ_{i}(N) = [γ_{i-1}(N), N]  has order {len(gi)}")
    if len(gi) == 1:
        print(f"  N is nilpotent of class {i-1}")
        break
    if len(gi) == len(gamma[i-1]):
        print(f"  Stabilized at γ_{i}(N) = γ_{i-1}(N)")
        break

# ── Upper central series ──────────────────────────────────────────────
print("\n=== UPPER CENTRAL SERIES ===")
# Z₀ = {e}, Z₁ = Z(N), Z₂/Z₁ = Z(N/Z₁), ...
Z = [{e}]
