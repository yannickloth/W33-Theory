print(f"  Elements of order 3 in N: {len(all_ord3_N)}")
print(f"  Subgroups of order 3 in N: {num_syl3_N}")

# The Fitting subgroup
# Since all Sylow-3 subgroups are conjugate (none normal individually unless there's just 1),
