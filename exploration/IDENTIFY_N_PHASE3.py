import json
from collections import Counter

def compose(a, b):
    return [a[b[i]] for i in range(len(a))]

def inv(p):
    q = [0]*len(p)
    for i, v in enumerate(p):
        q[v] = i
    return q

def perm_order(p):
    x = list(range(len(p)))
    q = list(p)
    for k in range(1, len(p)+1):
        if q == x:
            return k
        q = compose(p, q)
    return len(p)

# ── load N ────────────────────────────────────────────────────────────
with open("N_subgroup.json") as f:
    N_raw = json.load(f)
N_elts = [tuple(g) for g in N_raw]
N_set = set(N_elts)
e = tuple(range(len(N_elts[0])))

# ── conjugacy classes with element orders ─────────────────────────────
print("Computing conjugacy classes with element orders ...")
seen = set()
class_data = []  # (rep_order, class_size)
for g in N_elts:
    if g in seen:
        continue
    cls = set()
    for h in N_elts:
        c = tuple(compose(compose(list(h), list(g)), inv(list(h))))
        cls.add(c)
    rep_ord = perm_order(g)
    class_data.append((rep_ord, len(cls)))
    seen |= cls

class_data.sort(key=lambda x: (x[0], x[1]))
