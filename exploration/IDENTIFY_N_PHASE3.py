# Check if the core of a Sylow-2 subgroup is larger than C₂⁴
# The 2-core O₂(N) = largest normal 2-subgroup
# Find it: intersection of all Sylow-2 subgroups
# We know there are 3 Sylow-2 subgroups of order 64

print("\n  Computing O₂(N) (largest normal 2-subgroup) ...")
# Find all Sylow-2 subgroups
# Elements of 2-power order:
two_power = [g for g in N_elts if perm_order(g) in (1, 2, 4)]
print(f"  Elements of 2-power order: {len(two_power)}")

# From Pillar 95: Sylow-2 has 64 elements, 3 conjugates
# We need to find one Sylow-2 subgroup
# Use: start with an element of order 4, close under multiplication
# Actually, let's find a Sylow-2 subgroup by finding a subgroup of order 64
