# What about Sylow-3 in N? |N| = 192 = 64 × 3, so Sylow-3 of N has order 3.
# Number of Sylow-3 in N: divides 64, ≡ 1 mod 3 → {1, 4, 16, 64}
all_ord3_N = [g for g in N_elts if perm_order(g) == 3]
