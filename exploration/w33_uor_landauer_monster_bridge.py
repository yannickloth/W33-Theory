def build_uor_ring(n_bits):
    """Build the UOR ring Z/(2^n)Z and its dihedral generators."""
    modulus = 2**n_bits
    
    # The two involutions generating D_{2^n}
    neg = lambda x: (-x) % modulus       # Ring reflection
    bnot = lambda x: (modulus - 1) ^ x   # Hypercube reflection (bitwise complement)
    succ = lambda x: (x + 1) % modulus   # Successor
    
    # Critical identity: neg(bnot(x)) = succ(x) for all x
    critical_identity_holds = all(
        neg(bnot(x)) == succ(x) for x in range(modulus)
    )
    
    # Dihedral group order = 2 * modulus = 2^(n+1)
    dihedral_order = 2 * modulus
    
    return {
        'n_bits': n_bits,
        'modulus': modulus,
        'critical_identity': critical_identity_holds,
        'dihedral_order': dihedral_order,
        'neg': neg,
        'bnot': bnot,
        'succ': succ,
    }


